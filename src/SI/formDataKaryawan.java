private void autoNumber() {
    try {
        String sql = "SELECT MAX(RIGHT(Id,3)) AS no_auto FROM karyawan";
        st = con.createStatement();
        rs = st.executeQuery(sql);
        
        if (rs.next()) {
            String no_auto, nol_plus;
            int p;
            nol_plus = "";
            
            no_auto = Integer.toString(rs.getInt(1)+ 1);
            p = no_auto.length();
            for(int i=1;i<=3-p;i++) {
                nol_plus = nol_plus + 0;
            }
            
            txtId.setText("RJ"+ nol_plus + no_auto);
            
        } 
    } catch (Exception e) {
            txtId.setText("RJ001");

    }
}
