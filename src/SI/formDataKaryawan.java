    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed

        try {
            st = con.createStatement();
            if (txtId.getText().equals("")||
                txtNama.getText().equals("")||
                bagian.getSelectedItem().equals("")||
                txtNotelp.getText().equals("")||
                txtAlamat.getText().equals("")||
                txtTgl.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Data Tidak Boleh Kosong","Validasi Data",JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            if (simpan.getText() == "SIMPAN"){
                String cek = "SELECT * FROM karyawan WHERE Id = '"+ txtId.getText() +"'";
                rs = st.executeQuery(cek);
                if (rs.next()){
                    JOptionPane.showMessageDialog(null,"Ups Id ini Sudah Ada");
                } else {
                    String sql = "INSERT INTO karyawan VALUES ('"+ txtId.getText() +
                    "','" + txtNama.getText() +
                    "','" + bagian.getSelectedItem() +
                    "','" + txtNotelp.getText() +
                    "','" + jk.getSelectedItem()+
                    "','" + txtTgl.getText() +
                    "','" + txtAlamat.getText() + "')";
                    //                    String sql2 = "INSERT IGNORE INTO gaji_kry (Id, nama) VALUES ('"+ txtId.getText() +
                    //                            "','" + txtNama.getText() + "')";
                    st.executeUpdate(sql);
                    //                    st.executeUpdate(sql2);
                    JOptionPane.showMessageDialog(null,"Data Berhasil Disimpan");
                    bersih();
                }
            } else {
                String update = "UPDATE karyawan SET nama = '"+ txtNama.getText() +
                "', bagian = '"+ bagian.getSelectedItem() +
                "', telepon = '"+ txtNotelp.getText() +
                "', alamat = '"+ txtAlamat.getText() +
                "' WHERE Id = '"+ txtId.getText() + "'";
                //                String update2 = "UPDATE gaji_kry SET nama = '"+ txtNama.getText() +
                //                        "' WHERE Id = '"+ txtId.getText() +"'";
                st.executeUpdate(update);
                //                st.executeUpdate(update2);
                JOptionPane.showMessageDialog(null,"Data Berhasil Disimpan");
                bersih();
                tampilData()
                ;            }
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
        tampilData();
        autoNumber();
    }//GEN-LAST:event_simpanActionPerformed

