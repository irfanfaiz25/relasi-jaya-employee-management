    private void cariData(){
        try {
            st = con.createStatement();
            rs = st.executeQuery("SELECT * FROM karyawan WHERE "+ cari.getSelectedItem().toString() + " LIKE '%"+ txtCari.getText() +"%'");
            
            DefaultTableModel mdl = new DefaultTableModel();
            
            mdl.addColumn("No");
            mdl.addColumn("ID");
            mdl.addColumn("Nama");
            mdl.addColumn("Bagian");
            mdl.addColumn("No Telp");
            mdl.addColumn("JK");
            mdl.addColumn("Tgl Masuk");
            mdl.addColumn("Alamat");
                    
            mdl.getDataVector().removeAllElements();
            mdl.fireTableDataChanged();
            mdl.setRowCount(0);
            
            
            
            int no = 1;
            while (rs.next()) {
                Object[] data = {
                    no++,
                    rs.getString("Id"),
                    rs.getString("nama"),
                    rs.getString("bagian"),
                    rs.getString("telepon"),
                    rs.getString("JK"),
                    rs.getString("tgl_msk"),
                    rs.getString("alamat")
                };
                mdl.addRow(data);
                tabelKry.setModel(mdl);
            }
        } catch (Exception e) {
            
        }
    }
